	export default {
		data() {
			return {
				ruleForm: {
												shangpinmingcheng: '',
																fenlei: '',
																tupian: '',
																guige: '',
																pinpai: '',
																shengchanriqi: '',
																baozhiqi: '',
																xiangqing: '',
																clicktime: '',
																clicknum: '',
																price: '',
												},
																				fenleiOptions: [],
				fenleiIndex: 0,
																																																																																				// 登陆用户信息
				user: {},
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																																								
																		// 下拉框
			res = await this.$api.option(`shangpinfenlei`,`fenlei`,{});
			this.fenleiOptions = res.data;
																																																															
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`shangpinxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
															if(o=='shangpinmingcheng'){
					this.ruleForm.shangpinmingcheng = obj[o];
					continue;
					}
																				if(o=='fenlei'){
					this.ruleForm.fenlei = obj[o];
					continue;
					}
																				if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					continue;
					}
																				if(o=='guige'){
					this.ruleForm.guige = obj[o];
					continue;
					}
																				if(o=='pinpai'){
					this.ruleForm.pinpai = obj[o];
					continue;
					}
																				if(o=='shengchanriqi'){
					this.ruleForm.shengchanriqi = obj[o];
					continue;
					}
																				if(o=='baozhiqi'){
					this.ruleForm.baozhiqi = obj[o];
					continue;
					}
																				if(o=='xiangqing'){
					this.ruleForm.xiangqing = obj[o];
					continue;
					}
																				if(o=='clicktime'){
					this.ruleForm.clicktime = obj[o];
					continue;
					}
																				if(o=='clicknum'){
					this.ruleForm.clicknum = obj[o];
					continue;
					}
																				if(o=='price'){
					this.ruleForm.price = obj[o];
					continue;
					}
														}
																																																																																															}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																																								
			// 多级联动参数
																																																																								
																																							shengchanriqiChange(e) {
				this.ruleForm.shengchanriqi = e.target.value;
				this.$forceUpdate();
			},
																																				
																																																									// 日长控件选择日期时间
			clicktimeConfirm(val) {
				console.log(val)
				this.ruleForm.clicktime = val.result;
				this.$forceUpdate();
			},
																		
															// 下拉变化
			fenleiChange(e) {
				this.fenleiIndex = e.target.value
				this.ruleForm.fenlei = this.fenleiOptions[this.fenleiIndex]
			},
																																																												
																											tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																																																																	
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																																																																																								if((!this.ruleForm.shangpinmingcheng)){
					this.$utils.msg(`商品名称不能为空`);
					return
				}
																																																																																																																																																																																																																																																																																																												if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
																																				if((!this.ruleForm.price)){
					this.$utils.msg(`价格不能为空`);
					return
				}
																if(this.ruleForm.price&&(!this.$validate.isNumber(this.ruleForm.price))){
					this.$utils.msg(`价格应输入数字`);
					return
				}
																												if(this.ruleForm.id){
					await this.$api.update(`shangpinxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`shangpinxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

